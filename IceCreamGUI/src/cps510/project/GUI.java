    //TABLE CREATE Button
    private void jButton37ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton37ActionPerformed
        Boolean status=null;
        String tableName = jTextField61.getText();
        String data = jTextField74.getText();
        
        if(data.isEmpty()||data.isBlank()||tableName.isEmpty()||tableName.isBlank()){
            status=false;
        }
        else
        {
            JDBCOracleConnection jdbc = new JDBCOracleConnection();
            status= jdbc.createTable(tableName,data);
        }
        
        String result="";
        if(status){
            result="Create "+tableName+": Successful!";
        }
        else
        {
            result="Create "+tableName+": unsuccessful...";
        }
        System.out.println(jTextField61.getText());
        JOptionPane.showMessageDialog(null, result, "Result: Table Create ->"+tableName, JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton37ActionPerformed

