    //Customer Loyalty Totals
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String query = "SELECT * FROM CUSTOMER_LOYALTY_TOTALS";                        
        String[] columns = {"CUSTOMER_NAME","ORDERS_PLACED","TOTAL_TYPES_PRODUCT_PURCHASED","TOTAL_INGREDIENTS_USED"};
        
        JDBCOracleConnection jdbc = new JDBCOracleConnection();
        ArrayList<String> list = jdbc.getQuery(query, columns);
        
        String result = "";
        for(int i=0;i<columns.length+1;i++){
            if(i==0){
                result+="Columns:\n";
            }
            if(i==(columns.length-1)){
                result+=columns[i];
            }
            else if(i==columns.length){
                result+="\n\n";
            }
            else{
                result+=columns[i]+" | ";
            }
        }
        for(int i=0;i<list.size();i++){
            if(i==0){
                result+="Rows:\n";
            }
            result+=list.get(i)+"\n";
        }
        JOptionPane.showMessageDialog(null, result, "Result: Customer Loyalty Totals", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton4ActionPerformed

